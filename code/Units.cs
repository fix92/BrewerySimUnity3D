using UnityEngine;

namespace Units {

	[System.Serializable]
	public class Unit1 : System.Object
	{
		public string Name;
		public int Temp;
		public int Visco;
		public int Level;
		public string Stage;
		public string buttonString() {
			return Name + "\n" + Temp + "\n" + Visco + "\n" + Level + "\n" + Stage;
		}
		public override string ToString()
		{
			return "Name: " + Name + ", Data: " + Temp + " | " + Visco + " | " + Level + " | " + Stage;
		}
	}

